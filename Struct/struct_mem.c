#include <stdio.h>
/*
结构体在内存中的大小
 */
struct test
{    
    char x[13]; // 长度：13    占用：0-12
    int d;        // 4    16-19
    double f;     // 8    24-31
}ss;

int main(){
    printf("%d\n",sizeof(ss)); //输出32
    getchar();
    return 0;
}

